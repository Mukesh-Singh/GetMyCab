package gmc.com.getmycab.bean;

public class StateData{
    private String stateName;
    private String taxFrequency;
    private String stateTax;

    public String getStateName() {
        return stateName;
    }

    public void setStateName(String stateName) {
        this.stateName = stateName;
    }

    public String getTaxFrequency() {
        return taxFrequency;
    }

    public void setTaxFrequency(String taxFrequency) {
        this.taxFrequency = taxFrequency;
    }

    public String getStateTax() {
        return stateTax;
    }

    public void setStateTax(String stateTax) {
        this.stateTax = stateTax;
    }
}